# https://pl.spoj.com/problems/CALC/


class Dodawanie:
    def licz_sume(self, a, b):
        return a + b


class Odejmowanie:
    def licz_roznice(self, a, b):
        return a - b


class Mnozenie:
    def licz_iloczyn(self, a, b):
        return a * b


class Dzielenie:
    def licz_iloraz(self, a, b):
        return a / b


class Modulo:
    def licz_modulo(self, a, b):
        return a % b
